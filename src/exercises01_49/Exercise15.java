public class Exercise15 {
	public static void main(String[] args) {
		int a = 10;
		switch (a) {
			case 1:
				System.out.println("aは1");
				break;
			case 2:
				System.out.println("aは2");
				break;
			case 3:
				System.out.println("aは3");
				break;
			case 4:
				System.out.println("aは4");
				break;
			default:
				System.out.println("無効な入力");
				break;
		}

	}

}
